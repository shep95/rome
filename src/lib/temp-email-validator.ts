// Additional patterns to catch dynamically generated temp emails
const TEMP_EMAIL_PATTERNS = [
  /^\d+@.+/, // emails starting with numbers
  /^[a-z0-9]{10,}@[a-z0-9-]+\.(tk|ml|ga|cf)$/, // random strings on free domains
  /temp/i,
  /disposable/i,
  /throw.*away/i,
  /trash/i,
  /fake/i,
  /spam/i,
  /10min/i,
  /guerrilla/i,
];

export class TempEmailValidator {
  /**
   * Check if an email address is from a temporary email provider
   */
  static isTempEmail(email: string): boolean {
    if (!email || typeof email !== 'string') {
      return false;
    }

    const emailLower = email.toLowerCase().trim();
    const domain = emailLower.split('@')[1];
    
    if (!domain) {
      return false;
    }

    // Check against known temp email domains
    if (TEMP_EMAIL_DOMAINS.includes(domain)) {
      return true;
    }

    // Check against patterns
    for (const pattern of TEMP_EMAIL_PATTERNS) {
      if (pattern.test(emailLower)) {
        return true;
      }
    }

    return false;
  }

  /**
   * Get a user-friendly error message for temp email rejection
   */
  static getTempEmailErrorMessage(): string {
    return "Temporary email addresses are not allowed. Please use a permanent email address to create your account.";
  }

  /**
   * Validate email format and check if it's temporary
   */
  static validateEmail(email: string): { isValid: boolean; isTemp: boolean; error?: string } {
    if (!email || typeof email !== 'string') {
      return { isValid: false, isTemp: false, error: 'Email is required' };
    }

    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    if (!emailRegex.test(email)) {
      return { isValid: false, isTemp: false, error: 'Please enter a valid email address' };
    }

    const isTemp = this.isTempEmail(email);
    if (isTemp) {
      return { 
        isValid: false, 
        isTemp: true, 
        error: this.getTempEmailErrorMessage() 
      };
    }

    return { isValid: true, isTemp: false };
  }
}